package model;

public class UserVertex {
    private String id;
    private String data;



public UserVertex(String id) {
    this.id = id;

}

    public String getId() {
        return id;
    }
    public String getData() {
        return data;
    }


    public void setId(String id) {
        this.id = id;
    }

    public void setData(String data) {
        this.data = data;
    }
}

